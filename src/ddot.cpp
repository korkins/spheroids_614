double ddot(double const *x1, double const *x2, int const nx)
/*--------------------------------------------------------------------------------------------------
TASK:
	To compute dot product of two vectors.
IN:
	x1, x2   d[nx]   vectors
	nx       i       length of vectors
OUT:
	ddot     d       x1.x2
NOTE:
    'd' in 'ddot' means 'double'
REFS:
	-
--------------------------------------------------------------------------------------------------*/
{
	int
		ix;
	double
		dp;
//--------------------------------------------------------------------------------------------------
//
	dp = 0.0;
	for (ix = 0; ix < nx; ix++)
		dp += x1[ix] * x2[ix];
	return dp;
} // double ddot(...)
